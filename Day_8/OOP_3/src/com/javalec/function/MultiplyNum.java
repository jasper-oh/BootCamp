package com.javalec.function;

public class MultiplyNum {

	public int num1 , num2 ;
	
	public MultiplyNum(int num1 , int num2) {
		this.num1 = num1;
		this.num2 = num2;
				
	}
	
	public int multiplyNum(int i , int j) {
		
		return i * j;
	}
	
	public int multiplyNum2() {
		return num1 / num2;
	}
	
}
