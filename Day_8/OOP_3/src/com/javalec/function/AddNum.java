package com.javalec.function;

public class AddNum {
	
	
	// Field
	
	
	// Constructor
	public AddNum() {
		
	}
	
	

	// Method
	
	
	public int addAction(int x, int y ) {
		
		int sum = x + y;
		
		return sum;
	}
	
	public void addPrint(int x, int y) {
		int sum = x + y;
		System.out.println(sum);
	}
	
	
	
}
